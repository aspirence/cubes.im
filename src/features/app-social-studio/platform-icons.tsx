export function PlatformIcon({
  platform,
  size = 18,
  color,
}: {
  platform: string;
  size?: number;
  color?: string;
}) {
  const b = PLATFORM_BRANDS[platform];
  if (!b) return null;
  if (b.path) {
    return (
      <svg width={size} height={size} viewBox="0 0 24 24" fill={color ?? "currentColor"} aria-hidden>
        <path d={b.path} />
      </svg>
    );
  }
  return (
    <span style={{ fontSize: size * 0.62, fontWeight: 800, color: color ?? "currentColor", lineHeight: 1 }}>
      {b.mono}
    </span>
  );
}

